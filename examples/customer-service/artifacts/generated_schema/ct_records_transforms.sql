    ct_record_id,
    first_name,
    middle_name,
    last_name,
    name_suffix,
    ssn_hash,
    date_of_birth,
    gender,
    ethnicity,
    address_type,
    emergency_contact_name,
    emergency_contact_relation,
    drivers_license_number,
    dependents_count,
    language_preference,
    is_veteran,
    is_disabled,
    created_at,
    updated_at,
    source_code,
    fil1,
    fil2,
    created_at,
    updated_at
)
