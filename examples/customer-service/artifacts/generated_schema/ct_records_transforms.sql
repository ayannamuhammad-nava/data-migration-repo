    ct_recid,
    ct_fnam,
    ct_mnam,
    ct_lnam,
    ct_sufx,
    encode(sha256(ct_ssn::bytea), 'hex'),
    ct_dob,
    ct_gndr,
    ct_ethn,
    ct_adtyp,
    ct_emrg,
    ct_erel,
    ct_dln,
    ct_dpnds,
    ct_lang,
    ct_vetf,
    ct_disf,
    ct_crtdt,
    ct_upddt,
    ct_srccd,
    ct_fil1,
    ct_fil2,
    NOW(),
    NOW()
FROM contacts;
